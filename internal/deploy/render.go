func renderTunnelService(scope string, endpoint ClientEndpoint, tunnel ClientTunnel, description string) []byte {
	sshArgs := []string{
		"/usr/bin/ssh",
		"-N",
		"-L",
		fmt.Sprintf("%s:%d:%s:%d", tunnel.LocalHost, tunnel.LocalPort, tunnel.RemoteHost, tunnel.RemotePort),
		"-o",
		"ExitOnForwardFailure=yes",
		"-o",
		fmt.Sprintf("ServerAliveInterval=%d", endpoint.SSH.ServerAliveInterval),
		"-o",
		fmt.Sprintf("ServerAliveCountMax=%d", endpoint.SSH.ServerAliveCountMax),
	}
	if endpoint.SSH.Port != 22 {
		sshArgs = append(sshArgs, "-p", strconv.Itoa(endpoint.SSH.Port))
	}
	if endpoint.SSH.IdentityFile != "" {
		sshArgs = append(sshArgs, "-i", endpoint.SSH.IdentityFile)
	}
	sshArgs = append(sshArgs, endpoint.SSH.ExtraArgs...)
	sshArgs = append(sshArgs, fmt.Sprintf("%s@%s", endpoint.SSH.User, endpoint.SSH.Host))
	wantedBy := "default.target"
	if scope == ServiceScopeSystem {
		wantedBy = "multi-user.target"
	}

	return []byte(fmt.Sprintf(`[Unit]
Description=%s
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
ExecStart=%s
Restart=always
RestartSec=5

[Install]
WantedBy=%s
`, description, strings.Join(quoteSystemdArgs(sshArgs), " "), wantedBy))
}

