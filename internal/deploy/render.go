func joinInts(values []int) string {
	out := make([]string, 0, len(values))
	for _, value := range values {
		out = append(out, strconv.Itoa(value))
	}
	return strings.Join(out, ",")
}

func contains(values []string, target string) bool {
	for _, value := range values {
		if value == target {
			return true
		}
	}
	return false
}

func shellQuote(value string) string {
	return "'" + strings.ReplaceAll(value, "'", `'"'"'`) + "'"
}

func quoteSystemdArgs(values []string) []string {
	out := make([]string, 0, len(values))
	for _, value := range values {
		if value == "" {
			out = append(out, `""`)
			continue
		}
		if strings.ContainsAny(value, " \t\"") {
			escaped := strings.ReplaceAll(value, `\`, `\\`)
			escaped = strings.ReplaceAll(escaped, `"`, `\"`)
			out = append(out, `"`+escaped+`"`)
			continue
		}
		out = append(out, value)
	}
	return out
}

func primaryClientEndpoint(spec ClientConfig) ClientEndpoint {
	if len(spec.Endpoints) > 0 {
		return spec.Endpoints[0]
	}
	return ClientEndpoint{
		Name:   "primary",
		SSH:    spec.SSH,
		Tunnel: spec.Tunnel,
	}
}

func clientEndpoints(spec ClientConfig) []ClientEndpoint {
	if len(spec.Endpoints) > 0 {
		cloned := make([]ClientEndpoint, len(spec.Endpoints))
		copy(cloned, spec.Endpoints)
		return cloned
	}
	return []ClientEndpoint{primaryClientEndpoint(spec)}
}

func isMultiEndpointClient(spec ClientConfig) bool {
	return len(clientEndpoints(spec)) > 1
}

func clientServiceName(spec ClientConfig, endpoint ClientEndpoint) string {
	if !isMultiEndpointClient(spec) {
		return spec.ServiceName
	}
	return spec.ServiceName + "-" + endpoint.Name
}
