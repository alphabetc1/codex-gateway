func renderClientWrapper(wrapperName string, endpoints []clientWrapperEndpoint) []byte {
	usage := fmt.Sprintf("usage: %s [--endpoint name] <command> [args...]", wrapperName)
	if len(endpoints) == 0 {
		return []byte(fmt.Sprintf(`#!/usr/bin/env sh
set -eu
if [ "$#" -eq 0 ]; then
  printf '%s\n' >&2
  exit 1
fi
exec "$@"
`, usage))
	}

	var builder strings.Builder
	builder.WriteString("#!/usr/bin/env sh\n")
	builder.WriteString("set -eu\n")
	builder.WriteString("selected_endpoint=${CODEX_GATEWAY_ENDPOINT:-}\n")
	builder.WriteString("if [ \"${1:-}\" = \"--endpoint\" ]; then\n")
	builder.WriteString("  if [ \"$#\" -lt 2 ]; then\n")
	builder.WriteString("    printf " + shellQuote(usage+"\\n") + " >&2\n")
	builder.WriteString("    exit 1\n")
	builder.WriteString("  fi\n")
	builder.WriteString("  selected_endpoint=$2\n")
	builder.WriteString("  shift 2\n")
	builder.WriteString("fi\n")
	builder.WriteString("if [ \"$#\" -eq 0 ]; then\n")
	builder.WriteString("  printf " + shellQuote(usage+"\\n") + " >&2\n")
	builder.WriteString("  exit 1\n")
	builder.WriteString("fi\n")
	builder.WriteString("select_endpoint() {\n")
	builder.WriteString("  case \"$1\" in\n")
	for _, endpoint := range endpoints {
		builder.WriteString("    " + shellQuote(endpoint.Name) + ")\n")
		builder.WriteString("      selected_name=" + shellQuote(endpoint.Name) + "\n")
		builder.WriteString("      env_path=" + shellQuote(endpoint.EnvPath) + "\n")
		builder.WriteString("      return 0\n")
		builder.WriteString("      ;;\n")
	}
	builder.WriteString("  esac\n")
	builder.WriteString("  return 1\n")
	builder.WriteString("}\n")
	builder.WriteString("pick_endpoint() {\n")
	builder.WriteString("  if command -v nc >/dev/null 2>&1; then\n")
	for _, endpoint := range endpoints {
		builder.WriteString("    if nc -z " + shellQuote(endpoint.LocalHost) + " " + shellQuote(strconv.Itoa(endpoint.LocalPort)) + " >/dev/null 2>&1; then\n")
		builder.WriteString("      select_endpoint " + shellQuote(endpoint.Name) + "\n")
		builder.WriteString("      return 0\n")
		builder.WriteString("    fi\n")
	}
	builder.WriteString("  fi\n")
	builder.WriteString("  select_endpoint " + shellQuote(endpoints[0].Name) + "\n")
	builder.WriteString("}\n")
	builder.WriteString("if [ -n \"$selected_endpoint\" ]; then\n")
	builder.WriteString("  if ! select_endpoint \"$selected_endpoint\"; then\n")
	builder.WriteString("    printf 'unknown endpoint: %s\\n' \"$selected_endpoint\" >&2\n")
	builder.WriteString("    exit 1\n")
	builder.WriteString("  fi\n")
	builder.WriteString("else\n")
	builder.WriteString("  pick_endpoint\n")
	builder.WriteString("fi\n")
	builder.WriteString("export CODEX_GATEWAY_ACTIVE_ENDPOINT=$selected_name\n")
	builder.WriteString(". \"$env_path\"\n")
	builder.WriteString("exec \"$@\"\n")
	return []byte(builder.String())
}

